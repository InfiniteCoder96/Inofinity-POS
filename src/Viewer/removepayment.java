    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
            double ra = Double.parseDouble(ratxt.getText());
            double pay = Double.parseDouble(paytxt.getText());
            batxt.setText(pay-ra +  "");
            
            
            if(ra > pay){
                JOptionPane.showMessageDialog(this, "Remove Amount cannot be greater than Payment ");
            }else{
                
                try {
                    d.aud("insert into customer_payment (customer_id, date, payment, payment_type, extra, user_usename, previous, due , t) values('"+cidtxt.getText()+"' , NOW() , '"+(ra*-1)+"'  , 'RCash' , ' ' , 'dinesh' , '0' , '0' , '1' ) ");
                    d.aud("update customer set balance = balance + '"+ra+"' where id = '"+cidtxt.getText()+"' ");
                    
                    JOptionPane.showMessageDialog(this, "Payment Update Sucessfully");
                    
                    ratxt.setText("");
                    batxt.setText("");
                    cidtxt.setText("");
                    datetxt.setText("");
                    paytxt.setText("");
                    idtxt.setText("");
                    
                    searchpayment();
                    
                    
                } catch (Exception e) {
                    e.printStackTrace();
                }
            
            }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void ratxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ratxtKeyTyped
        char c = evt.getKeyChar();
        
        String key = String.valueOf(c);
        
        if  (!(Character.isDigit(c) || key.equals(".") )) {
            evt.consume();
	    Toolkit.getDefaultToolkit().beep();
        } 
        
        if(ratxt.getText().contains(".") && key.equals(".")){
            evt.consume();
	    Toolkit.getDefaultToolkit().beep();
        }
    }//GEN-LAST:event_ratxtKeyTyped

