    private void loadtable() {
    
        dtm.setRowCount(0);
        try {
            double cb = 0.0;
            ResultSet rs = d.srh("select product.id , product.name , supplier.name , avg(lowstock) as ls , sum(avqty) as aq from stock\n" +
"inner join product on product.id = stock.product_id\n" +
"inner join supplier on stock.supplier_id = supplier.id\n" +
"group by stock.product_id\n" +
"Having avg(lowstock) >= sum(avqty)");
            while (rs.next()) { 
               java.util.Vector v = new java.util.Vector();
               v.add(rs.getString("id"));
               v.add(rs.getString("product.name"));
               v.add(rs.getString("supplier.name"));
               v.add(rs.getString("aq"));
               dtm.addRow(v);
               
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    
    }
