    private void loadtablecustomer() {
         dtm.setRowCount(0);
    
        try {
            
            String Selectedname = "id";
            
            if(jComboBox1.getSelectedItem().toString().equals("id")){
            Selectedname = "id";
                System.out.println("NAME");
            }else  if(jComboBox1.getSelectedItem().toString().equals("Name")){
            Selectedname = "name";
            
            }else  if(jComboBox1.getSelectedItem().toString().equals("tel")){
            Selectedname = "tel";
            }
            
            ResultSet rs = d.srh("select * from customer where "+Selectedname+" LIKE '%"+searchtxtcu.getText()+"%' ");
            
             while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("tel"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    
    
    
    
    }

