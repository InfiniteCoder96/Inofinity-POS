    private void loadtablewarranty() {
        dtm.setRowCount(0);
        try {
            
            String selecteditem = "imie";
            
            if(jComboBox1.getSelectedItem().equals("IMIE")){
                selecteditem = "imie";
            }else if(jComboBox1.getSelectedItem().equals("Barcode")){
                selecteditem = "stock_product_barcode";
            }else if(jComboBox1.getSelectedItem().equals("Name")){
                selecteditem = "name";
            }
            
            ResultSet rs = d.srh("SELECT * FROM warranty inner join product on warranty.stock_product_barcode = product.barcode where "+selecteditem+" LIKE '"+searchtxt.getText()+"%'  ");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("imie"));
                v.add(rs.getString("stock_product_barcode"));
                v.add("");
                v.add(rs.getString("period"));
                v.add(rs.getString("start"));
                v.add(rs.getString("end"));
                v.add(rs.getString("invoiceid"));
                dtm.addRow(v);
                
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    
    }

    private void searchewarranty() {
    
        if(jTable1.getSelectedRow() == -1){
            
        }else{
            int selectedrow = jTable1.getSelectedRow();
            String imie = jTable1.getValueAt(selectedrow, 0).toString();
            String pid = jTable1.getValueAt(selectedrow, 1).toString();
            String inv = jTable1.getValueAt(selectedrow, 6).toString();
            
            try {
                ResultSet rs1 = d.srh("select * from product where barcode = '"+pid+"' ");
                while (rs1.next()) {                    
                    pidtxt.setText( "<html>" + rs1.getString("barcode") + "<br>" + rs1.getString("name") + "<br>" + rs1.getString("catagary_name") + "</html>"  );
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            
            try {
                ResultSet rs2 = d.srh("SELECT * FROM invoice_reg inner join customer on customer_number = number where id = '"+inv+"' ");
                while (rs2.next()) {      
                    System.out.println("1");
                    invlbl.setText( "<html> Invoice No# : " + rs2.getString("id") + "<br> " + rs2.getString("name") + "<br>" + rs2.getString("number") + "</html>"  );
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            
            try {
                ResultSet rs = d.srh("SELECT * FROM warranty where imie = '"+imie+"'  ");
                    while (rs.next()) {                
                        startlbl.setText(rs.getString("start"));
                        endlbl.setText(rs.getString("end"));
                    }
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
    
    }
