    private void loadallcatagary() {
        dtm.setRowCount(0);
        try {
            ResultSet rs = d.srh("select * from brand ");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getObject("name"));
                v.add(rs.getObject("keycode"));
                dtm.addRow(v);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    
    }

    private void save() {
    
        try {
                
            if(catagarytxt.getText().isEmpty()){

            }else if (d.checkavailable("select * from brand where name = '"+catagarytxt.getText()+"' ", "name") == 1){
                JOptionPane.showMessageDialog(this, "This Catagary already added");
                catagarytxt.requestFocus();
            }else if (d.checkavailable("select * from brand where keycode = '"+keytxt.getText()+"' ", "keycode") == 1){
                JOptionPane.showMessageDialog(this, "This KEYCODE already added");
                keytxt.requestFocus();
            }else{
                    d.aud("insert into brand(name , keycode , lastid) values ('"+catagarytxt.getText()+"' , '"+keytxt.getText()+"' , '100' )");
                    JOptionPane.showMessageDialog(this, "Saved Successfully");
                    catagarytxt.setText("");
                    keytxt.setText("");
                    loadallcatagary();
            }

            } catch (Exception e) {
                    e.printStackTrace();
            }
    
    }
