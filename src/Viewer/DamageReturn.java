    private void printReport(int returnid) {
        try {
            //1 compile jrxm file
        JasperReport jr = JasperCompileManager.compileReport(getClass().getResourceAsStream("/Reports/Return-reciept.jrxml"));
        //2insert paramters
        java.util.Map m = new java.util.HashMap();
        m.put("ID", returnid);
        //3 run engine
        JasperPrint jp = JasperFillManager.fillReport(jr, m, d.getConnection());
        //4 view result
        //JasperViewer.viewReport(jp, false);
        //JRViewer jv = new JRViewer(jp);
        JasperPrintManager.printReport(jp, true);
        //
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }

    private void searchproduct() {
    
        try {
                    numberlist1.setVisible(true);
                    numberlist2.setVisible(true);
                    jlm1.removeAllElements();
                    ResultSet rs = d.srh("select *  from product where id LIKE '%"+cpidtxt.getText()+"%' OR name LIKE '%"+cpidtxt.getText()+"%' ");
                    for (int j = 0; rs.next(); j++) {
                        jlm1.add(j, rs.getString("id") + " - " + rs.getString("name") );
                    }
                    numberlist1.setModel(jlm1);

                    if(numberlist1.getModel().getSize() == 0){
                        numberlist1.setVisible(false);
                        clear(2);
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }
    
    }
    
