private void searchex()
  {
    try
    {
      this.dtm.setRowCount(0);
      
      String a = "date(now())";
      if (this.jComboBox1.getSelectedIndex() == 1) {
        a = "DATE_ADD( date(now()),INTERVAL 10 DAY)";
      } else if (this.jComboBox1.getSelectedIndex() == 2) {
        a = "DATE_ADD( date(now()),INTERVAL 31 DAY)";
      } else if (this.jComboBox1.getSelectedIndex() == 3) {
        a = "DATE_ADD( date(now()),INTERVAL 90 DAY)";
      } else if (this.jComboBox1.getSelectedIndex() == 4) {
        a = "DATE_ADD( date(now()),INTERVAL 180 DAY)";
      }
      ResultSet rs = this.d.srh("select * from stock inner join product on stock.product_id = product.id where exdate <= " + a + " and avqty > 0 order by exdate");
      while (rs.next())
      {
        Vector v = new Vector();
        v.add(rs.getString("product_id"));
        v.add(rs.getString("name"));
        v.add(rs.getString("stock.id"));
        v.add(Double.valueOf(rs.getDouble("buyprice")));
        v.add(Double.valueOf(rs.getDouble("avqty")));
        v.add(rs.getString("exdate"));
        this.dtm.addRow(v);
      }
    }
    catch (Exception e)
    {
      e.printStackTrace();
    }
  }
