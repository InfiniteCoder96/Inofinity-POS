    private void loadtable() {
        
        try {
            dtm.setRowCount(0);
            
            ResultSet rs = d.srh("SELECT * FROM stock inner join product on "
                    + "stock.product_id = product.id "
                    + "inner join supplier on stock.supplier_id = supplier.id "
                    + "where stock.product_id = '"+cpidtxt.getText()+"'  ");
            while (rs.next()) {                
                Vector v = new Vector();
                v.add(rs.getString("product_id"));
                v.add(rs.getString("product.name"));
                v.add(rs.getString("stock.id"));
                v.add(rs.getDouble("buyprice"));
                v.add(rs.getDouble("avqty"));
                v.add(rs.getDouble("rqty"));
                v.add(rs.getString("exdate"));
                v.add(rs.getString("supplier.name"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    
    }


    private void savereturn() {
        try {
            
            if(invtable.getSelectedRow() == -1){
                JOptionPane.showMessageDialog(this, "Please Select item first");
            }else if(jTextField1.getText() == ""){
                JOptionPane.showMessageDialog(this, "Please insert qty");
            }else{
                    
                    int selectedrow = invtable.getSelectedRow();
                    String stockid = invtable.getValueAt(selectedrow, 2).toString();
                    
                    int i = d.aud("update stock set active = 2 , rlist = '"+jTextField1.getText()+"' where id = '"+stockid+"' ");
                    
                    
                    if(i == 1 ){
                        JOptionPane.showMessageDialog(this, "Return Added Sucess");
                        jTextField1.setText("");
                    }else{
                        JOptionPane.showMessageDialog(this, "Something Went Wrong ...");
                    }
                    
                    loadtable();
                }      
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }

