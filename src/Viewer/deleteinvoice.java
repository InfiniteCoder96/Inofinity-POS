    private void searchproduct() {
        dtm.setRowCount(0);
        try {
            ResultSet rs = d.srh("select * from invoice_reg where id = '"+pidtxt.getText()+"' and date(date) = CURDATE() ");
            
            while(rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("date"));
                v.add(rs.getString("user_usename"));
                v.add(rs.getString("grandtotal"));
                dtm.addRow(v);           
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
                
    }

